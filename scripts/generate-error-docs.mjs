// Generates apps/web/content/docs/errors.mdx from the DAWN_ERRORS registry so
// the public error-code reference can never drift from the code. Reads the
// built @dawn-ai/sdk dist (run after `pnpm build`). The check-docs guard
// enforces that the committed output lists exactly the registry's codes.
import { writeFileSync } from "node:fs"
import { resolve } from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"

const repoRoot = resolve(fileURLToPath(import.meta.url), "..", "..")
const sdkDist = pathToFileURL(resolve(repoRoot, "packages/sdk/dist/index.js")).href
const outPath = resolve(repoRoot, "apps/web/content/docs/errors.mdx")

const { DAWN_ERRORS } = await import(sdkDist)

const rows = Object.values(DAWN_ERRORS)
  .sort((a, b) => a.code.localeCompare(b.code))
  .map((descriptor) => {
    const docs = descriptor.docsPath ? `[${descriptor.docsPath}](${descriptor.docsPath})` : "—"
    return `| \`${descriptor.code}\` | ${descriptor.title} | ${docs} |`
  })

const body = `{/* GENERATED by scripts/generate-error-docs.mjs — do not edit by hand. */}

# Error codes

Every user-facing Dawn failure carries a stable \`DAWN_Exxxx\` code. The code is
printed on the surface where the failure appears — CLI stderr, HTTP/SSE error
bodies, and permission-denial tool results — so a failure is searchable and
linkable regardless of how you hit it.

Codes are grouped by range: \`E1xxx\` config / \`dawn check\`, \`E2xxx\` sandbox,
\`E3xxx\` permissions, \`E4xxx\` model / provider, and \`E5xxx\` runtime / import.
A code may omit a docs link and still be a valid, searchable identifier.

| Code | Meaning | Docs |
| --- | --- | --- |
${rows.join("\n")}
`

writeFileSync(outPath, body)
console.log(`[generate-error-docs] wrote ${rows.length} code(s) to ${outPath}`)
