const ADD_A_TOOL = `Help me add a new tool to an existing Dawn app. Tools are co-located with the route that uses them and their types are auto-generated by Dawn's TypeScript compiler API integration — no Zod schemas, no manual type wiring.

1. Inside an existing route directory (e.g. the default scaffold's \`src/app/research/\`), create a \`tools/\` subdirectory if one doesn't exist. Add a new TypeScript file with a default export that is an async function:

   \`\`\`ts
   // src/app/research/tools/<tool-name>.ts
   export default async (input: { readonly /* fields */ }) => {
     // do work
     return { /* output */ }
   }
   \`\`\`

2. The input parameter type and the return type are both inferred. Dawn extracts them at build time and writes them into \`.dawn/dawn.generated.d.ts\`. The tool becomes available on the \`ctx.tools\` object in the route's workflow/graph with full autocomplete, and \`dawn build\` wires it into generated agent entries.

3. Run \`dawn typegen\` to regenerate types after adding the tool (or leave \`dawn dev\` running — it does this on every file save).

4. For a \`workflow\` or \`graph\` route, update \`index.ts\` to call the new tool via \`ctx.tools.<tool-name>({ ... })\` and show me the diff. For an \`agent\` route, leave \`index.ts\` as the default \`agent(...)\` descriptor and rely on \`dawn build\` to wire the tool into the generated graph.

5. Re-run the route with \`dawn run\` and confirm the new tool is invoked end-to-end.

Constraints:
- The default export must be a function (arrow or async function declaration).
- Input and output types must be serializable as JSON.
- \`readonly\` is recommended on input fields; Dawn preserves it through type generation.

Reference: https://dawnai.org/llms.txt
`;

const WRITE_A_ROUTE = `Help me add a new route to an existing Dawn app. Routes are directories under \`src/app/\` where each directory maps to a URL-style pathname (minus route groups).

1. Create the route directory. For a route under a dynamic \`[topic]\` segment:
   \`\`\`
   src/app/<new-route>/[topic]/
   \`\`\`

2. Optionally create \`state.ts\` — the route's Zod state schema:
   \`\`\`ts
   import { z } from "zod"

   export default z.object({
     question: z.string().default(""),
   })
   \`\`\`

3. Create \`index.ts\` — the route entry. Export exactly ONE of:

   **Workflow** (async function, most common):
   \`\`\`ts
   import type { RuntimeContext } from "@dawn-ai/sdk"
   import type { RouteTools } from "dawn:routes"
   import type { z } from "zod"
   import type state from "./state.js"

   type <RouteName>State = z.infer<typeof state> & { readonly topic: string }

   export async function workflow(
     state: <RouteName>State,
     ctx: RuntimeContext<RouteTools<"/<route-path>">>
   ) {
     // use ctx.tools.<toolName>() — fully typed
     return { ...state, /* updates */ }
   }
   \`\`\`

   **Graph** (LangGraph graph/workflow):
   \`\`\`ts
   export const graph = /* langgraph graph instance */
   \`\`\`

   **Chain** (LangChain LCEL Runnable):
   \`\`\`ts
   export const chain = /* LCEL runnable */
   \`\`\`

   **Agent** (default descriptor):
   \`\`\`ts
   import { agent } from "@dawn-ai/sdk"

   export default agent({
     model: "gpt-5-mini",
     systemPrompt: "You are a helpful assistant.",
   })
   \`\`\`

4. Optionally create a \`tools/\` directory with co-located tools (see the "Add a tool" prompt).

5. Run \`dawn routes\` to confirm Dawn discovered the new route and what pathname it computed. Then \`dawn run '<pathname>'\` with the required state via stdin.

Constraints:
- Exactly one of default \`agent(...)\`, named \`workflow\`, named \`graph\`, or named \`chain\` may be exported from \`index.ts\`.
- Route groups in parentheses \`(public)\` are NOT part of the pathname.
- Dynamic segment values, such as \`topic\`, come from the JSON input when invoking the parameterized route id.
- The \`RouteTools<"/path">\` type is auto-generated from tools in the route's \`tools/\` directory.

Reference: https://dawnai.org/llms.txt
`;

const WRITE_A_TEST = `Help me write tests for a Dawn route. Pick the right style for the route kind:

1. For an agent route like the default \`/research#agent\`, write a Vitest test with \`createAgentHarness\`, \`script()\` fixtures, and agent matchers:

   \`\`\`ts
   import { fileURLToPath } from "node:url"
   import { afterAll, it } from "vitest"
   import { createAgentHarness, expectFinalMessage, expectToolCalled, script } from "@dawn-ai/testing"

   const appRoot = fileURLToPath(new URL("..", import.meta.url))
   const h = await createAgentHarness({ appRoot, route: "/research#agent" })
   afterAll(() => h.close())

   it("searches the corpus and writes a cited answer", async () => {
     h.reset()
     const run = await h.run({
       input: "What are common agent architectures?",
       fixtures: script()
         .user("What are common agent architectures?")
         .callsTool("searchCorpus", { query: "agent architectures" })
         .callsTool("readDoc", { path: "corpus/agent-architectures.md" })
         .replies("ReAct and plan-and-execute are common. [corpus/agent-architectures.md]"),
     })

     expectToolCalled(run, "searchCorpus")
     expectToolCalled(run, "readDoc")
     expectFinalMessage(run).toContain("[corpus/")
   }, 60_000)
   \`\`\`

2. For deterministic \`workflow\`, \`graph\`, or \`chain\` routes, use a colocated \`run.test.ts\` scenario file:

   \`\`\`ts
   export default [
     {
       name: "returns a greeting",
       input: { tenant: "acme" },
       expect: {
         status: "passed",
         output: { tenant: "acme", greeting: "Hello, acme!" },
       },
     },
   ]
   \`\`\`

3. In scenario records, \`input\` is the route state and \`expect.output\` is the returned state. Keep these for deterministic route-output assertions, not LLM text exact matches.

4. Run agent Vitest files with the package's test runner (for the scaffold, \`npm test\`). Run declarative scenario records with:
   \`\`\`
   dawn test
   \`\`\`

5. To run a scenario record against a live \`dawn dev\` server, set \`run: { url: "http://127.0.0.1:3001" }\` on that scenario. There is no command-level \`--url\` flag on \`dawn test\`.

Constraints:
- Agent tests should use fixtures or live mode; do not exact-match raw assistant message arrays with \`expect.output\`.
- \`run.test.ts\` must live in the deterministic route's directory, default-export an array, and avoid \`describe()\` / \`test()\` wrappers.
- Per-scenario tool mocking is not supported today; stub external dependencies inside the tool implementation or use the agent harness fixture tools.

Reference: https://dawnai.org/llms.txt
`;

