export type PromptSlug =
	| "scaffold"
	| "add-a-tool"
	| "write-a-route"
	| "write-a-test"
	| "deploy";

export interface PromptEntry {
	readonly slug: PromptSlug;
	readonly title: string;
	readonly description: string;
	readonly body: string;
}

